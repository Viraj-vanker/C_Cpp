#include<iostream>
using namespace std;
int main()
{
	int a=10,b=20;
	
	cout<<"less than  = "<<(a<b)<<endl;
	cout<<"greater than  = "<<(a>b)<<endl;
	cout<<"less than equal = "<<(a<=b)<<endl;
	cout<<"greater than equal = "<<(a>=b)<<endl;
	cout<<"equals equal = "<<(a==b)<<endl;
	cout<<"not equal to = "<<(a!=b)<<endl;
}
